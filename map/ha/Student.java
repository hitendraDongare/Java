package hashMapTest;

public class Student {
	
	private int rollNo;
	private String name, address;
	public Student(int rollNo, String name, String address) {
		super();
		this.rollNo = rollNo;
		this.name = name;
		this.address = address;
	}
	public int getRollNo() {
		return rollNo;
	}
	public String getName() {
		return name;
	}
	public String getAddress() {
		return address;
	}
	
	

}
